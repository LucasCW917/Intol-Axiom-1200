#ifndef OPCODES_H
#define OPCODES_H

#include <stdint.h>

/* Opcode format: [32-bit namespace] x [32-bit opcode]
   All built-in opcodes live in namespace 0.
   We store them as uint64_t: high 32 = namespace, low 32 = opcode id */

#define MAKE_OPCODE(ns, op) (((uint64_t)(ns) << 32) | (uint64_t)(op))

/* ── Math (0x0001–0x0008) ─────────────────────────────────── */
#define OP_ADD   MAKE_OPCODE(0, 0x00000001)
#define OP_SUB   MAKE_OPCODE(0, 0x00000010)
#define OP_MUL   MAKE_OPCODE(0, 0x00000011)
#define OP_DIV   MAKE_OPCODE(0, 0x00000100)
#define OP_SHL   MAKE_OPCODE(0, 0x00000101)
#define OP_SHR   MAKE_OPCODE(0, 0x00000110)
#define OP_NEG   MAKE_OPCODE(0, 0x00000111)
#define OP_SAV   MAKE_OPCODE(0, 0x00001000)

/* ── Logic (0x0009–0x000D) ────────────────────────────────── */
#define OP_IFEQ  MAKE_OPCODE(0, 0x00001001)
#define OP_IFNQ  MAKE_OPCODE(0, 0x00001010)
#define OP_IFTR  MAKE_OPCODE(0, 0x00001011)
#define OP_IFFL  MAKE_OPCODE(0, 0x00001100)
#define OP_WHLE  MAKE_OPCODE(0, 0x00001101)

/* ── User-Defined Functions ───────────────────────────────── */
#define OP_DEF   MAKE_OPCODE(0, 0x00001110)
#define OP_TAKE  MAKE_OPCODE(0, 0x00010000)
#define OP_RET   MAKE_OPCODE(0, 0x00010001)

/* ── Branching ────────────────────────────────────────────── */
#define OP_STP   MAKE_OPCODE(0, 0x0000001111111010ULL)
#define OP_JMP   MAKE_OPCODE(0, 0x0000001111111011ULL)

/* ── I/O & String ─────────────────────────────────────────── */
#define OP_INP   MAKE_OPCODE(0, 0x0000010000000000ULL)
#define OP_UGST  MAKE_OPCODE(0, 0x0000010000000001ULL)
#define OP_GSTR  MAKE_OPCODE(0, 0x0000010000000010ULL)
#define OP_TCHA  MAKE_OPCODE(0, 0x0000001111111101ULL)
#define OP_TINT  MAKE_OPCODE(0, 0x0000001111111110ULL)
#define OP_OUT   MAKE_OPCODE(0, 0x0000001111111111ULL)
#define OP_SAPP  MAKE_OPCODE(0, 0x0000010000110000ULL)
#define OP_DSTR  MAKE_OPCODE(0, 0x0000010000110001ULL)
#define OP_WRAW  MAKE_OPCODE(0, 0x0000010000110010ULL)
#define OP_ITOS  MAKE_OPCODE(0, 0x0000010000101110ULL)
#define OP_STOI  MAKE_OPCODE(0, 0x0000010000101111ULL)

/* ── Collections ──────────────────────────────────────────── */
#define OP_LEN   MAKE_OPCODE(0, 0x0000010000010000ULL)
#define OP_SAVL  MAKE_OPCODE(0, 0x0000010000010001ULL)
#define OP_SAVD  MAKE_OPCODE(0, 0x0000010000010010ULL)
#define OP_GET   MAKE_OPCODE(0, 0x0000010000010100ULL)
#define OP_FORK  MAKE_OPCODE(0, 0x0000010000010101ULL)
#define OP_JOIN  MAKE_OPCODE(0, 0x0000010000010110ULL)
#define OP_LOCK  MAKE_OPCODE(0, 0x0000010000010111ULL)
#define OP_UNLO  MAKE_OPCODE(0, 0x0000010000011000ULL)
#define OP_AADD  MAKE_OPCODE(0, 0x0000010000011001ULL)
#define OP_YIEL  MAKE_OPCODE(0, 0x0000010000011010ULL)
#define OP_THID  MAKE_OPCODE(0, 0x0000010000011011ULL)
#define OP_THST  MAKE_OPCODE(0, 0x0000010000011100ULL)
#define OP_ASUB  MAKE_OPCODE(0, 0x0000010000011101ULL)
#define OP_AMUL  MAKE_OPCODE(0, 0x0000010000011110ULL)
#define OP_ADIV  MAKE_OPCODE(0, 0x0000010000011111ULL)

/* ── Bitwise ──────────────────────────────────────────────── */
#define OP_NAND  MAKE_OPCODE(0, 0x0000010000000011ULL)
#define OP_NOT   MAKE_OPCODE(0, 0x0000010000000100ULL)
#define OP_AND   MAKE_OPCODE(0, 0x0000010000000101ULL)
#define OP_OR    MAKE_OPCODE(0, 0x0000010000000110ULL)
#define OP_NOR   MAKE_OPCODE(0, 0x0000010000000111ULL)
#define OP_XOR   MAKE_OPCODE(0, 0x0000010000001000ULL)
#define OP_XNOR  MAKE_OPCODE(0, 0x0000010000001001ULL)
#define OP_BUF   MAKE_OPCODE(0, 0x0000010000001010ULL)

/* ── Exception Handling ───────────────────────────────────── */
#define OP_TRY   MAKE_OPCODE(0, 0x0000010000001011ULL)
#define OP_THW   MAKE_OPCODE(0, 0x0000010000001100ULL)

/* ── Time ─────────────────────────────────────────────────── */
#define OP_GTM   MAKE_OPCODE(0, 0x0000010000001101ULL)
#define OP_CTM   MAKE_OPCODE(0, 0x0000010000001110ULL)
#define OP_STAL  MAKE_OPCODE(0, 0x0000010000001111ULL)

/* ── Buffer (internal) ────────────────────────────────────── */
#define OP_BUFF  MAKE_OPCODE(0, 0x00001073ULL)

/* ── QoL Extensions ───────────────────────────────────────── */
#define OP_MOD   MAKE_OPCODE(0, 0x0000010001110011ULL)
#define OP_ABS   MAKE_OPCODE(0, 0x0000010001110100ULL)
#define OP_MIN   MAKE_OPCODE(0, 0x0000010001110101ULL)
#define OP_MAX   MAKE_OPCODE(0, 0x0000010001110110ULL)
#define OP_SLEN  MAKE_OPCODE(0, 0x0000010001110111ULL)
#define OP_SCMP  MAKE_OPCODE(0, 0x0000010001111000ULL)
#define OP_ISNUM MAKE_OPCODE(0, 0x0000010001111001ULL)
#define OP_CLRL  MAKE_OPCODE(0, 0x0000010001111010ULL)
#define OP_DELD  MAKE_OPCODE(0, 0x0000010001111011ULL)
#define OP_DBG   MAKE_OPCODE(0, 0x0000010001111100ULL)

#endif /* OPCODES_H */